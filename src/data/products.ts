/** Build the compact product catalog string to inject into AI prompt */
export function buildProductCatalogPrompt(): string {
  const byCategory: Record<string, CuratedProduct[]> = {};
  for (const p of CURATED_PRODUCTS) {
    if (!byCategory[p.category]) byCategory[p.category] = [];
    byCategory[p.category].push(p);
  }

  const categoryLabels: Record<string, string> = {
    shampoo: "SHAMPOOS",
    conditioner: "CONDITIONERS",
    "leave-in": "LEAVE-IN CONDITIONERS",
    "curl-cream": "CURL CREAMS & STYLING CREAMS",
    gel: "GELS & CUSTARDS",
    oil: "OILS & SERUMS",
    mask: "DEEP CONDITIONERS & MASKS",
  };

  const lines: string[] = [
    "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━",
    "CURATED PRODUCT DATABASE (validated 2024–2025):",
    "Strongly prefer recommending specific products from this list when they match the hair profile.",
    "Use the exact product name and brand from the database. Only use generic descriptions if no catalog product fits.",
    "Entry format: • \"Product Name\" by Brand | PRICE | Retailers | CGM | Porosity | Curl types | Key concerns",
    "PRICE: $=budget(<$12) $$=mid($12-32) $$$=luxury(>$32)",
    "",
  ];

  const order = ["shampoo", "conditioner", "leave-in", "curl-cream", "gel", "oil", "mask"];
  for (const cat of order) {
    const products = byCategory[cat];
    if (!products?.length) continue;
    lines.push(`${categoryLabels[cat] ?? cat.toUpperCase()}:`);
    for (const p of products) {
      const cgm = p.cgmSafe ? "CGM✓" : "CGM✗";
      const porosity = p.bestFor.porosity?.join("/") ?? "all";
      const curls = p.bestFor.curlTypes?.join(",") ?? "all";
      const concerns = p.bestFor.hairConcerns?.join(",") ?? "";
      const retailers = p.whereToBuy.slice(0, 3).join("/");
      lines.push(
        `• "${p.name}" by ${p.brand} | ${p.priceRange} | ${retailers} | ${cgm} | porosity:${porosity} | curls:${curls} | ${concerns}`
      );
    }
    lines.push("");
  }

  return lines.join("\n");
}
